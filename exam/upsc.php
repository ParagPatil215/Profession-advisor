        <h2 class="text-xl font-bold mb-2 text-gray-800">UPSC Exam: What is the UPSC Civil Services Examination?</h2>
        <p class="text-gray-700 leading-relaxed mb-4">Civil Services Examination (CSE) is one of the most popular exams conducted by the Union Public Service Commission. It is widely known as the 'IAS exam' even though CSE is a common exam to recruit candidates to about 24 top government services like IAS, IPS, IFS, IRS etc.</p>

        <p class="text-gray-700 leading-relaxed mb-6">UPSC Civil Services Exam (CSE) consists of 3 stages. The stages are:</p>
        <ol class="list-decimal pl-6 mb-6">
            <li class="text-gray-700">Preliminary Exam (objective)</li>
            <li class="text-gray-700">Main Exam (written)</li>
            <li class="text-gray-700">Interview (personality test)</li>
        </ol>

        <p class="text-gray-700 leading-relaxed mb-6">The exam window extends around 10-12 months (usually from June month of a year to June month next year when the results are announced).</p>

