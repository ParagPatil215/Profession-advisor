        <h1 class="text-2xl font-bold mb-4 text-gray-800">What is NEET?</h1>
        <p class="text-gray-700 leading-relaxed mb-6">The National Eligibility cum Entrance Test (NEET) is an entrance examination in India for students who wish to study undergraduate medical courses (MBBS) and dental courses (BDS) in government or private medical colleges and dental colleges in India.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">NEET Exam Pattern</h2>
        <p class="text-gray-700 leading-relaxed mb-6">NEET is a pen-and-paper based test conducted for a duration of 3 hours. The exam consists of multiple-choice questions (MCQs) from subjects like Physics, Chemistry, and Biology (Botany & Zoology). There are a total of 180 questions, with 45 questions from each subject.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">Eligibility Criteria</h2>
        <p class="text-gray-700 leading-relaxed mb-6">To appear for NEET, candidates must have passed their 10+2 examination with Physics, Chemistry, Biology/Biotechnology, and English as compulsory subjects. The minimum age limit for candidates is 17 years, and there is no upper age limit for appearing in NEET.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">NEET Syllabus</h2>
        <p class="text-gray-700 leading-relaxed mb-6">The syllabus for NEET is based on the common syllabus notified by the Medical Council of India (MCI). It includes topics covered in the 10+2 curriculum of various education boards like CBSE, ICSE, and State Boards.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">Preparation</h2>
        <p class="text-gray-700 leading-relaxed">Preparation for NEET requires a thorough understanding of concepts, regular practice, and solving previous years' question papers and mock tests. Many candidates also join coaching institutes or online courses for additional preparation.</p>
