        </ul>

        <h2 class="text-xl font-bold mb-2 text-gray-800">MPSC Exam Pattern</h2>
        <p class="text-gray-700 leading-relaxed mb-6">The exam pattern for MPSC exams varies depending on the specific exam. Generally, the exams consist of multiple choice questions (MCQs) on subjects relevant to the respective services. There are usually preliminary exams followed by main exams and interviews for selected candidates.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">Eligibility Criteria</h2>
        <p class="text-gray-700 leading-relaxed mb-6">The eligibility criteria for MPSC exams vary for different exams and services. Generally, candidates must have a bachelor's degree from a recognized university. Age limits and other criteria also apply and may vary for different exams.</p>

