        <h1 class="text-2xl font-bold mb-4 text-gray-800">What is JEE Mains & Advanced?</h1>
        <p class="text-gray-700 leading-relaxed mb-6">The Joint Entrance Examination (JEE) is an all-India level engineering entrance exam conducted for admission to various undergraduate engineering programs in India. JEE consists of two parts: JEE Mains and JEE Advanced.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">JEE Mains</h2>
        <p class="text-gray-700 leading-relaxed mb-6">JEE Mains is conducted by the National Testing Agency (NTA) for admission to undergraduate engineering programs at NITs, IIITs, and other centrally funded technical institutions, as well as for admission to engineering programs at institutions in states like Maharashtra, Madhya Pradesh, Odisha, and others.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">JEE Advanced</h2>
        <p class="text-gray-700 leading-relaxed mb-6">JEE Advanced is conducted by one of the IITs for admission to undergraduate programs at the Indian Institutes of Technology (IITs). Only the top 2,50,000 candidates (including all categories) who qualify JEE Mains are eligible to appear for JEE Advanced.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">JEE Exam Pattern</h2>
        <p class="text-gray-700 leading-relaxed mb-6">The exam pattern for both JEE Mains and JEE Advanced includes multiple-choice questions (MCQs) and numerical-based questions on subjects like Physics, Chemistry, and Mathematics. The exams are conducted in online mode.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">Eligibility Criteria</h2>
        <p class="text-gray-700 leading-relaxed mb-6">The eligibility criteria for JEE Mains and JEE Advanced differ slightly. Generally, candidates must have passed their 10+2 examination with Physics, Chemistry, and Mathematics as compulsory subjects. Age limits and other criteria may also apply.</p>

        <h2 class="text-xl font-bold mb-2 text-gray-800">Preparation</h2>
        <p class="text-gray-700 leading-relaxed">Preparation for JEE exams requires a solid understanding of concepts, regular practice, and solving previous years' question papers and mock tests. Many candidates also join coaching institutes for additional preparation.</p>
